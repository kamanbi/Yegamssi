ELEM_RE = re.compile(r'^([a-z]+)_([A-Z][0-9]*)_([a-z]+)_(df|ex)_([a-z]+)$')

INPUT  = r"F:\Yegamssi\etc\fortune_styles\fortune_ko_humor.csv"
OUTPUT = r"F:\Yegamssi\etc\fortune_styles\fortune_ko_humor.csv"
BACKUP = r"F:\Yegamssi\etc\fortune_styles\backup\fortune_ko_humor_before_elem.csv"

pathlib.Path(BACKUP).parent.mkdir(parents=True, exist_ok=True)
shutil.copy2(INPUT, BACKUP)
print(f"Backup → {BACKUP}")

out_rows = []
rows_changed = rows_skipped = rows_state = 0

with open(INPUT, newline='', encoding='utf-8-sig') as f:
    reader = csv.reader(f)
    header = next(reader)
    out_rows.append(header)
    for row in reader:
        if len(row) < 4:
            out_rows.append(row); continue
        code, typ, text, *rest = row
        weight = rest[0] if rest else "1"
        m = ELEM_RE.match(code)
        if not m:
            out_rows.append(row); continue
        cat, tier, ohaeng, dfex, weather = m.groups()
        if typ == "state":
            rows_state += 1; out_rows.append(row); continue
        if typ not in ("intro", "effect", "action"):
            out_rows.append(row); continue
        try:
            new_text = ELEM_NEW[cat][tier][ohaeng][typ]
        except KeyError:
            rows_skipped += 1; out_rows.append(row); continue
        if new_text != text:
            rows_changed += 1
        out_rows.append([code, typ, new_text, weight])

with open(OUTPUT, 'w', newline='', encoding='utf-8-sig') as f:
    csv.writer(f, quoting=csv.QUOTE_MINIMAL).writerows(out_rows)

unique = set()
cnt = 0
with open(OUTPUT, newline='', encoding='utf-8-sig') as f:
    for row in csv.reader(f):
        if len(row) < 3: continue
        code, typ, text = row[0], row[1], row[2]
        if ELEM_RE.match(code) and typ in ("intro","effect","action"):
            unique.add(text); cnt += 1

