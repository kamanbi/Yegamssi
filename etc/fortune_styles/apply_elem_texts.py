print(f"")
print(f"ELEM intro/effect/action 행수 : {elem_intro_effect_action_count:,}")
print(f"고유 텍스트 수               : {len(unique_elem_texts):,}  ← 540 목표")
print(f"\n완료: {OUTPUT_PATH}")
