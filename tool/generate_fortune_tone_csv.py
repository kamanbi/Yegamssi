def overflow_nuance(suffix: int) -> str:
    prefixes = [
        "가까운",
        "작은",
        "뒤따르는",
        "남은",
        "숨은",
        "늦게 오는",
        "옆의",
        "다음",
        "조용한",
        "미묘한",
    ]
    nouns = [
        "변수",
        "기척",
        "신호",
        "흐름",
        "여지",
        "반응",
        "무게",
        "결",
        "방향",
        "조짐",
    ]
    prefix = prefixes[(suffix - 1) % len(prefixes)]
    noun = nouns[((suffix - 1) // len(prefixes)) % len(nouns)]
    return f"{prefix} {noun}"


