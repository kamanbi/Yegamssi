@dataclass(frozen=True)
class FortuneRow:
    code: str
    type: str
    text: str
    weight: str
    index: int
    occurrence: int

    @property
    def parts(self) -> list[str]:
        return self.code.split("_")

    @property
    def category(self) -> str:
        return self.parts[0]

    @property
    def tier(self) -> str:
        return self.parts[1] if len(self.parts) > 1 else "C"

    @property
    def element(self) -> str:
        return self.parts[2] if len(self.parts) > 2 else ""

    @property
    def strength(self) -> str:
        return self.parts[3] if len(self.parts) > 3 else ""

    @property
    def weather(self) -> str:
        return self.parts[4] if len(self.parts) > 4 else ""


