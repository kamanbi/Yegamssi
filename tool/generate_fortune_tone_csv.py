def read_base_rows(path: Path) -> list[FortuneRow]:
    if not path.exists():
        raise SystemExit(f"기본 파일이 없습니다: {path}")

    with path.open("r", encoding="utf-8-sig", newline="") as file:
        reader = csv.DictReader(file)
        if reader.fieldnames != EXPECTED_COLUMNS:
            raise SystemExit(f"컬럼 불일치: {reader.fieldnames}")

        occurrence_by_key: dict[tuple[str, str], int] = defaultdict(int)
        rows: list[FortuneRow] = []
        for index, raw in enumerate(reader):
            key = (raw["code"], raw["type"])
            occurrence = occurrence_by_key[key]
            occurrence_by_key[key] += 1
            rows.append(
                FortuneRow(
                    code=raw["code"],
                    type=raw["type"],
                    text=raw["text"],
                    weight=raw["weight"],
                    index=index,
                    occurrence=occurrence,
                )
            )
    if not rows:
        raise SystemExit("기본 파일에 행이 없습니다.")
    return rows


def generate_rows(tone: str, rows: list[FortuneRow]) -> list[dict[str, str]]:
    seen_texts: set[str] = set()
    generated: list[dict[str, str]] = []
    for row in rows:
        text = build_text(tone, row)
        text = ensure_unique_text(tone, row, text, seen_texts)
        generated.append(
            {
                "code": row.code,
                "type": row.type,
                "text": text,
                "weight": row.weight,
            }
        )
    return generated


def build_text(tone: str, row: FortuneRow) -> str:
    if tone == "tsundere":
        return build_tsundere(row)
    if tone == "cynical":
        return build_purpose_fragment("cynical", row)
    if tone == "emotional":
        return build_purpose_fragment("emotional", row)
    if tone == "historical":
        return build_purpose_fragment("historical", row)
    if tone == "ai":
        return build_purpose_fragment("ai", row)
    raise ValueError(tone)


