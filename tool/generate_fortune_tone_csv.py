def validate_output(base_rows: list[FortuneRow], rows: list[dict[str, str]]) -> ToneReport:
    if len(base_rows) != len(rows):
        raise SystemExit(f"행 수 불일치: base={len(base_rows)} output={len(rows)}")

    empty = 0
    code_leaks = 0
    for base, generated in zip(base_rows, rows, strict=True):
        if base.code != generated["code"]:
            raise SystemExit(f"code 불일치: {base.code} != {generated['code']}")
        if base.type != generated["type"]:
            raise SystemExit(f"type 불일치: {base.type} != {generated['type']}")
        if base.weight != generated["weight"]:
            raise SystemExit(f"weight 불일치: {base.weight} != {generated['weight']}")
        if not generated["text"].strip():
            empty += 1
        if FORBIDDEN_CODE_WORDS.search(generated["text"]):
            code_leaks += 1

    texts = [row["text"] for row in rows]
    if len(texts) != len(set(texts)):
        raise SystemExit("중복 문구가 남아 있습니다.")
    if empty:
        raise SystemExit(f"빈 문구가 있습니다: {empty}")
    if code_leaks:
        raise SystemExit(f"내부 코드명 노출이 있습니다: {code_leaks}")

    starts = Counter(text[:START_CHECK_LENGTH] for text in texts)
    repeated_starts = starts.most_common(10)
    return ToneReport(
        rows=len(rows),
        empty=empty,
        unique_texts=len(set(texts)),
        code_leaks=code_leaks,
        repeated_starts=repeated_starts,
    )


def stable_hash(*values: object) -> int:
    text = "|".join(str(value) for value in values)
    total = 0
    for char in text:
        total = (total * 131 + ord(char)) % 1_000_000_007
    return total


